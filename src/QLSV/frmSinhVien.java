    public ArrayList<SinhVien>layDanhSachSinhVien(){
       ArrayList<SinhVien>lstSinhVien = new ArrayList<SinhVien>();
       Connection con = getConnection();
       try{
           st = (Statement) con.createStatement();
           /*Thực thi câu lệnh truy vấn*/
           String sql = "SELECT * FROM sinhvien";
           ResultSet rs = st.executeQuery(sql);
           SinhVien sv;
           while(rs.next()){
               sv = new SinhVien(rs.getInt("id"),rs.getString("MaSV"),rs.getString("HoVaTen"),rs.getDouble("DiemTB"));
               lstSinhVien.add(sv);
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
       return lstSinhVien;
    }
    
    public void hienThiDanhSachSinhVien(){
        String colTieuDe[] = new String[]{"ID","Mã SV","Họ Và Tên","Điểm TB"};
        
        ArrayList<SinhVien>lstSinhVien = layDanhSachSinhVien();
        
        DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
        
        Object[]row;
        for(int i=0;i<lstSinhVien.size();i++){
            row = new Object[4];
            
            //gán giá trị:
            row[0] = lstSinhVien.get(i).getiD();
            row[1] = lstSinhVien.get(i).getMaSV();
            row[2] = lstSinhVien.get(i).getHoTen();
            row[3] = lstSinhVien.get(i).getDiemTB();
            
            //thêm vào model:
            model.addRow(row);
        }
        
    }
    
